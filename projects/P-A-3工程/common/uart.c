#include "uart.h"

/*
 * 串口初始化
 * 波特率 4800, 方式1 (8位UART)
 * 使用定时器2作为波特率发生器 (1T模式)
 *
 * 波特率计算公式 (1T模式):
 *   BaudRate = Fosc / (4 * (65536 - [T2H,T2L]))
 *   4800 = 11059200 / (4 * (65536 - 0xFDC0))
 *         = 11059200 / (4 * 576) = 4800
 */
void uart_init(void)
{
    SCON = 0x50;                     /* 方式1, 8位UART, 允许接收 */
    AUXR |= 0x01;                    /* S1ST2=1: 定时器2作为波特率发生器 */
    T2L = 0xC0;                      /* 波特率低字节 */
    T2H = 0xFD;                      /* 波特率高字节 */
    AUXR |= 0x10;                    /* T2x12=1: 定时器2 1T模式 */
    AUXR |= 0x04;                    /* T2R=1: 启动定时器2 (最后启动) */
    ES = 1;                          /* 允许串口中断 */
    EA = 1;                          /* 开总中断 */
}

/*
 * 串口发送一个字节
 * dat: 要发送的字节数据
 */
void uart_send_byte(u8 dat)
{
    SBUF = dat;                      /* 写入发送缓冲 */
    while (!TI);                     /* 等待发送完成 */
    TI = 0;                          /* 清除发送完成标志 */
}

/*
 * 检查是否收到数据
 * 返回值: 1=有数据, 0=无数据
 */
u8 uart_received(void)
{
    return RI ? 1 : 0;               /* RI=1表示收到数据 */
}

/*
 * 读取串口接收到的字节
 * 返回值: 接收到的字节数据
 */
u8 uart_get_char(void)
{
    while (!RI);                     /* 等待接收完成 */
    RI = 0;                          /* 清除接收标志 */
    return SBUF;                     /* 返回接收数据 */
}

